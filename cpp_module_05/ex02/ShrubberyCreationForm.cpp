ShrubberyCreationForm::ShrubberyCreationForm(std::string target): Form("ShrubberyCreation", target, 145, 137) {}

ShrubberyCreationForm::ShrubberyCreationForm(ShrubberyCreationForm const &copy): Form(copy) {}

ShrubberyCreationForm::~ShrubberyCreationForm() {}

ShrubberyCreationForm const &ShrubberyCreationForm::operator=(ShrubberyCreationForm const &copy) {
	return *static_cast<ShrubberyCreationForm*>(&Form::operator=(copy));
}

void ShrubberyCreationForm::run() const {
	struct timeval time_now;
  gettimeofday(&time_now, 0);
  time_t msecs_time = (time_now.tv_sec * 1000) + (time_now.tv_usec / 1000);
  srand(static_cast<unsigned int>(msecs_time));
  
  std::ofstream file(std::string(this->getTarget()+"_shrubbery").c_str());
	file << trees[rand() % 5];
	file.close();
}
